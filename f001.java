public class f001 {
public static void main(String f[]) {
		int f01=Integer.parseInt(f[0]);
		int f02=Integer.parseInt(f[1]);
		int f03=Integer.parseInt(f[2]);
			System.out.println( "Plus="+(f01+f02+f03));
			System.out.println( "Minus="+(f01-f02-f03));
			System.out.println( "Multi="+(f01*f02*f03));
			System.out.println( "Div="+(f01/f02/f03));
		}
}

